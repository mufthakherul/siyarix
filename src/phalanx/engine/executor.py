class ExecutionEngine:
    """The core execution engine.

    Combines model-driven autonomous planning with registry-based execution.
    Supports three modes: registry, autonomous, and integrated (default).

    Usage::

        engine = ExecutionEngine()
        result = await engine.execute("scan 192.168.1.1 with nmap then analyze results")
    """

    def __init__(
        self,
        mode: ExecutionMode = ExecutionMode.INTEGRATED,
        registry: ToolRegistry | None = None,
        config: dict[str, Any] | None = None,
        learning_memory: Any = None,
        session_logger: Any = None,
    ) -> None:
        self._mode = mode
        self._registry = registry or ToolRegistry()
        self._config = config or {}

        # Discover tools and build context
        fast_discovery = bool(self._config.get("fast_discovery"))
        self._discovered_tools = self._registry.discover(fast=fast_discovery)
        self._tool_map: dict[str, ToolInfo] = {
            t.name: t for t in self._discovered_tools
        }
        self._binary_map: dict[str, ToolInfo] = {
            t.binary: t for t in self._discovered_tools
        }

        # Initialize task planner with providers
        self._planner = TaskPlanner()
        self._setup_providers()

        # Runtime intelligence services (XI)
        self._xi_tracker = ContextTracker()
        self._xi_predictor = Predictor()
        self._replan_attempts = 0
        self._max_replans = int(self._config.get("max_replans", 3))

        # Bounded sub-agent pool
        max_subagents = int(self._config.get("max_subagents", 15))
        self._pool = AsyncWorkerPool(max_workers=max_subagents)

        # Initialize dynamic resolver
        registered = {t.binary: t.path for t in self._discovered_tools}
        registered.update({t.name: t.path for t in self._discovered_tools})
        self._resolver = DynamicResolver(registered_tools=registered)

        # Shared graph and step state
        self._graph = KnowledgeGraph()

        # Tool executor (delegates actual tool/shell runs)
        self._executor = ToolExecutor(
            resolver=self._resolver,
            discovered_tools=self._discovered_tools,
            graph=self._graph,
        )

        # Step results for tracking
        self._completed_steps: dict[str, StepResult] = {}
        self._offline_store: OfflineStore | None = None

        # Learning memory for correction tracking
        self._learning_memory = learning_memory

        # Session logger for structured audit logging
        self._session_logger = session_logger
        self._current_log_session_id: str | None = None

        # Lazy XI services (SkillProfiler & XICoreService)
        self._xi_skill_profiler: SkillProfiler | None = None
        self._xi_core_service: XICoreService | None = None

        # Kill switch for emergency stop
        self._kill_switch = KillSwitch()
        self._kill_switch.register(self._on_kill)

    @property
    def kill_switch(self) -> KillSwitch:
        return self._kill_switch

    def _on_kill(self) -> None:
        """Callback when kill switch is triggered."""
        logger.warning("Kill switch triggered - cancelling all pool tasks")
        try:
            self._pool.cancel_pending()
        except Exception as exc:
            logger.exception("Pool cancellation failed: %s", exc)

    def _get_offline_store(self) -> OfflineStore:
        """Get or create offline store instance."""
        if self._offline_store is None:
            self._offline_store = OfflineStore()
        return self._offline_store

    def _get_skill_profiler(self) -> SkillProfiler:
        if self._xi_skill_profiler is None:
            self._xi_skill_profiler = SkillProfiler()
        return self._xi_skill_profiler

    def _get_xi_core_service(self) -> XICoreService:
        if self._xi_core_service is None:
            self._xi_core_service = XICoreService()
        return self._xi_core_service

    def _refresh_tools(self) -> None:
        """Force rediscovery of installed tools and rebuild the resolver."""
        fast_discovery = bool(self._config.get("fast_discovery"))
        self._discovered_tools = self._registry.discover(
            force_refresh=True, fast=fast_discovery
        )
        self._tool_map = {t.name: t for t in self._discovered_tools}
        self._binary_map = {t.binary: t for t in self._discovered_tools}

        registered = {t.binary: t.path for t in self._discovered_tools}
        registered.update({t.name: t.path for t in self._discovered_tools})
        self._resolver = DynamicResolver(registered_tools=registered)

    async def _try_install_tool(self, tool_name: str) -> bool:
        """Attempt to automatically install a required tool after obtaining user permission."""
        system = platform.system().lower()
        package_names = {
            "nmap": "nmap",
            "masscan": "masscan",
            "amass": "amass",
            "subfinder": "subfinder",
            "dnsx": "dnsx",
            "theHarvester": "theharvester",
            "whois": "whois",
            "shodan": "shodan",
            "nikto": "nikto",
            "gobuster": "gobuster",
            "ffuf": "ffuf",
            "feroxbuster": "feroxbuster",
            "httpx": "httpx",
            "wpscan": "wpscan",
            "sqlmap": "sqlmap",
            "nuclei": "nuclei",
            "hydra": "hydra",
            "john": "john",
            "hashcat": "hashcat",
            "msfconsole": "metasploit-framework",
            "trufflehog": "trufflehog",
            "gitleaks": "gitleaks",
            "aws": "awscli",
            "az": "azure-cli",
            "gcloud": "google-cloud-sdk",
            "docker": "docker",
            "podman": "podman",
            "kubectl": "kubernetes-cli",
            "helm": "helm",
            "terraform": "terraform",
            "ansible": "ansible",
            "uv": "uv",
        }

        package = package_names.get(tool_name, tool_name)
        install_commands: list[list[str]] = []

        python_tools = {"shodan", "trufflehog", "ansible", "theHarvester"}
        if tool_name in python_tools or package == "shodan":
            install_commands.append(["pip", "install", "--upgrade", package])
            install_commands.append(["pip3", "install", "--upgrade", package])
            install_commands.append(
                ["python", "-m", "pip", "install", "--upgrade", package]
            )
            install_commands.append(
                ["python3", "-m", "pip", "install", "--upgrade", package]
            )

        if system == "windows":
            install_commands.append(
                [
                    "winget",
                    "install",
                    "-e",
                    "--id",
                    (
                        f"SecurityTool.{package}"
                        if package in ["nmap", "wireshark"]
                        else package
                    ),
                    "--silent",
                    "--accept-package-agreements",
                    "--accept-source-agreements",
                ]
            )
            install_commands.append(["winget", "install", package, "--silent"])
            install_commands.append(
                ["choco", "install", package, "-y", "--no-progress"]
            )
        elif system == "darwin":
            install_commands.append(["brew", "install", package])
        else:
            install_commands.append(["sudo", "apt-get", "update", "-y"])
            install_commands.append(["sudo", "apt-get", "install", "-y", package])
            install_commands.append(["apt-get", "install", "-y", package])

        from ..output import output

        valid_installers = []
        for cmd in install_commands:
            installer_bin = cmd[0]
            if shutil.which(installer_bin):
                valid_installers.append(cmd)

        if not valid_installers:
            console.print(
                f"[yellow]⚠ Cannot automatically install '{tool_name}': No package manager (winget/choco/brew/apt/pip) found on PATH.[/yellow]"
            )
            return False

        installer_desc = " / ".join([" ".join(c) for c in valid_installers[:2]])
        confirm_msg = f"Tool '{tool_name}' is required but not installed. Do you want to automatically install it? (Proposed: {installer_desc})"

        if not output.prompt_confirm(confirm_msg, default=True):
            console.print(
                f"[yellow]✗ Skipping installation of '{tool_name}' by user request.[/yellow]"
            )
            return False

        console.print(
            f"[bold blue]⚡ Initiating auto-installation of '{tool_name}'...[/bold blue]"
        )

        success = False
        for cmd in valid_installers:
            console.print(f"[dim]Running: {' '.join(cmd)}[/dim]")
            try:
                from phalanx.engine import run_tool_complete as _run_tool_complete
                result = await _run_tool_complete(cmd[0], cmd[1:], timeout=300)
                if result.exit_code == 0:
                    success = True
                    console.print(
                        f"[bold green]✓ Successfully installed '{tool_name}' via '{cmd[0]}'![/bold green]"
                    )
                    break
                else:
                    console.print(
                        f"[yellow]⚠ Installer '{cmd[0]}' exited with code {result.exit_code}. Trying next installer...[/yellow]"
                    )
            except Exception as e:
                console.print(
                    f"[dim]Installer '{cmd[0]}' encountered an error: {e}[/dim]"
                )

        if success:
            self._refresh_tools()
            return True
        else:
            console.print(
                f"[red]✗ Failed to install '{tool_name}'. Please install it manually.[/red]"
            )
            return False


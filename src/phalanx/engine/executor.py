    def discovered_tools(self) -> list[ToolInfo]:
        return self._discovered_tools

    def _build_context(self) -> dict[str, Any]:
        """Build context dict for the task planner."""
        recent_execs = self._xi_tracker.recent_executions
        last_tool = recent_execs[-1].tool if recent_execs else ""
        prediction_objs = self._xi_predictor.predict_next(
            phase=self._xi_tracker.phase,
            last_tool=last_tool,
            target="",
            findings_count=self._xi_tracker.total_findings,
        )

        sp = self._get_skill_profiler()
        xi_skill = {
            "level": sp.profile.level,
            "verbosity": sp.profile.verbosity,
            "show_hints": sp.profile.show_hints,
            "score": sp.profile.score,
        }

        xi_recs: list[dict[str, str]] = []
        try:
            xics = self._get_xi_core_service()
            from ..core.intent_router import IntentRoute
            from ..core.session_kernel import SessionContext

            session = SessionContext(session_id="engine", objective="")
            route = IntentRoute(instruction="", mode=self._mode.value)
            xi_recs = [
                {"title": r.title, "reason": r.reason, "priority": r.priority}
                for r in xics.recommend(session, route)
            ]
        except Exception as exc:
            logger.debug("Failed to get XI recommendations: %s", exc)

        return {
            "available_tools": [
                {
                    "name": t.name,
                    "binary": t.binary,
                    "capabilities": t.capabilities,
                    "version": t.version,
                }
                for t in self._discovered_tools
            ],
            "mode": self._mode.value,
            "xi_context": self._xi_tracker.summary(),
            "xi_predictions": [
                {"action": p.action, "confidence": p.confidence, "reason": p.reason}
                for p in prediction_objs
            ],
            "xi_skill": xi_skill,
            "xi_recommendations": xi_recs,
        }

    async def plan(self, instruction: str) -> ExecutionPlan:
        """Create an execution plan for the given instruction.

        This is the planning phase — no execution happens here.
        """
        context = self._build_context()
        context = self.compress_context(context)
        force_mode = None
        if self._mode == ExecutionMode.REGISTRY:
            force_mode = "static"
        elif self._mode == ExecutionMode.AUTONOMOUS:
            force_mode = "autonomous"

        return await self._planner.plan(instruction, context, force_mode)

    async def execute(
        self,
        instruction: str,
        interactive: bool = True,
        dry_run: bool = False,
        persist: bool = False,
    ) -> EngineResult:
        """Plan and execute a natural language instruction.

        Parameters
        ----------
        instruction:
            The user's natural language command.
        interactive:
            If True, show progress and prompt for unsafe commands.
        dry_run:
            If True, plan only — don't execute.
        """
        start_time = time.monotonic()
        persist_workflow = persist or bool(self._config.get("persist_workflows"))
        store = self._get_offline_store() if persist_workflow else None
        plan_id: str | None = None

        if self._kill_switch.state == KillSwitchState.TRIGGERED:
            logger.warning("Execution aborted: kill switch triggered")
            return EngineResult(
                plan=ExecutionPlan(instruction=instruction, steps=[]),
                mode=self._mode,
                step_results=[],
                total_duration_ms=0.0,
            )

        plan = await self.plan(instruction)

        if interactive:
            self._display_plan(plan)

        if persist_workflow:
            plan_id = str(uuid.uuid4())
            assert store is not None
            store.save_plan(
                plan_id=plan_id,
                instruction=instruction,
                plan_json=json.dumps(plan.to_dict()),
                status="planned",
            )

        if self._session_logger is not None:
            log_id = plan_id or str(uuid.uuid4())[:12]
            self._session_logger.create_log(
                session_id=log_id,
                persona=self._config.get("persona", ""),
                llm_provider=self._config.get("model_provider", ""),
                llm_model=self._config.get("gemini_model", ""),
            )
            self._current_log_session_id = log_id
            self._session_logger.add_command(
                session_id=log_id,
                input_text=instruction,
                ai_plan=[
                    s.command or s.tool or "" for s in plan.steps if s.command or s.tool
                ],
            )

        if dry_run:
            result = EngineResult(plan=plan, mode=self._mode)
            result.plan_id = plan_id
            return result

        if not plan.steps:
            if interactive:
                console.print(
                    "[yellow]Could not create an execution plan from your instruction.[/yellow]\n"
                    "[dim]Tip: Try being more specific, e.g. "
                    "'scan 192.168.1.1 with nmap and nuclei'[/dim]"
                )
            result = EngineResult(plan=plan, mode=self._mode)
            result.plan_id = plan_id
            return result

        self._completed_steps.clear()
        self._replan_attempts = 0
        result = EngineResult(plan=plan, mode=self._mode)
        result.plan_id = plan_id

        if persist_workflow and plan_id:
            assert store is not None
            store.update_plan_status(plan_id, status="running")

        if interactive:
            with Progress(
                SpinnerColumn(),
                TextColumn("[bold cyan]{task.description}"),
                TimeElapsedColumn(),
                console=console,
            ) as progress:
                result = await self._execute_plan(
                    plan, progress, interactive, plan_id, store
                )
        else:
            result = await self._execute_plan(plan, None, interactive, plan_id, store)

        result.plan_id = plan_id

        result.total_duration_ms = (time.monotonic() - start_time) * 1000

        metrics = get_metrics()
        total_findings = len(result.all_findings)
        metrics.record_scan(
            duration=result.total_duration_ms / 1000,
            successful=result.success,
            findings_count=total_findings,
        )

        if persist_workflow and plan_id:
            assert store is not None
            store.update_plan_status(
                plan_id,
                status="completed" if result.success else "failed",
                completed=True,
            )

        if interactive:
            self._display_summary(result)

        if self._session_logger is not None and self._current_log_session_id:
            self._session_logger.update_end_time(self._current_log_session_id)
            for sr in result.step_results:
                for f in sr.findings or []:
                    tool = f.get("tool", "unknown")
                    self._session_logger.track_tool_usage(
                        self._current_log_session_id, tool
                    )
            self._current_log_session_id = None

        return result

    async def resume(self, plan_id: str, interactive: bool = True) -> EngineResult:
        """Resume a previously persisted execution plan."""
        store = self._get_offline_store()
        plan_record = store.get_plan(plan_id)
        if not plan_record:
            raise ValueError(f"Plan not found: {plan_id}")

        plan_data = json.loads(plan_record.get("plan_json", "{}"))
        plan = self._plan_from_dict(plan_data)

        self._completed_steps.clear()
        self._replan_attempts = 0
        for row in plan_record.get("steps", []):
            findings = json.loads(row.get("findings_json") or "[]")
            sr = StepResult(
                step_id=row.get("step_id", ""),
                status=StepStatus(row.get("status", StepStatus.FAILED.value)),
                output=row.get("output", "") or "",
                error=row.get("error", "") or "",
                duration_ms=float(row.get("duration_ms") or 0.0),
                findings=findings,
                retry_count=int(row.get("retry_count") or 0),
                exit_code=row.get("exit_code"),
            )
            self._completed_steps[sr.step_id] = sr

        start_time = time.monotonic()
        store.update_plan_status(plan_id, status="running")
        if interactive:
            with Progress(
                SpinnerColumn(),
                TextColumn("[bold cyan]{task.description}"),
                TimeElapsedColumn(),
                console=console,
            ) as progress:
                result = await self._execute_plan(
                    plan, progress, interactive, plan_id, store
                )
        else:
            result = await self._execute_plan(plan, None, interactive, plan_id, store)
        result.total_duration_ms = (time.monotonic() - start_time) * 1000
        result.plan_id = plan_id

        metrics = get_metrics()
        metrics.record_scan(
            duration=result.total_duration_ms / 1000,
            successful=result.success,
            findings_count=len(result.all_findings),
        )

        store.update_plan_status(
            plan_id,
            status="completed" if result.success else "failed",
            completed=True,
        )

        if interactive:
            self._display_summary(result)

        return result

    def _plan_from_dict(self, data: dict[str, Any]) -> ExecutionPlan:
        """Reconstruct an ExecutionPlan from stored JSON data."""
        steps: list[ExecutionStep] = []
        for s in data.get("steps", []):
            step_type_value = s.get("step_type", StepType.TOOL_RUN.value)
            try:
                step_type = StepType(step_type_value)
            except ValueError:
                step_type = StepType.TOOL_RUN
            steps.append(
                ExecutionStep(
                    id=s.get("id", ""),
                    step_type=step_type,
                    tool=s.get("tool"),
                    command=s.get("command"),
                    args=s.get("args", []) or [],
                    target=s.get("target"),
                    depends_on=s.get("depends_on", []) or [],
                    condition=s.get("condition"),
                    timeout=int(s.get("timeout", 300)),
                    description=s.get("description", ""),
                    metadata=s.get("metadata", {}) or {},
                )
            )

        return ExecutionPlan(
            steps=steps,
            source=data.get("source", "registry"),
            confidence=float(data.get("confidence", 0.0)),
            raw_instruction=data.get("raw_instruction", ""),
            interpreted_task=None,
        )

    async def _execute_plan(
        self,
        plan: ExecutionPlan,
        progress: Progress | None,
        interactive: bool,
        plan_id: str | None,
        store: OfflineStore | None,
    ) -> EngineResult:
        """Execute all steps in the plan respecting dependencies.

        Steps with no unmet dependencies are executed in parallel via
        asyncio.gather.  Steps whose dependencies have all completed are
        scheduled in the next wave.  This gives automatic parallelism for
        independent branches while preserving ordering for dependent steps.
        """
        result = EngineResult(plan=plan, mode=self._mode)
        pending = list(plan.steps)

        while pending:
            ready: list[ExecutionStep] = []
            still_pending: list[ExecutionStep] = []

            for step in pending:
                if step.id in self._completed_steps:
                    sr = self._completed_steps[step.id]
                    result.step_results.append(sr)
                    result.all_findings.extend(sr.findings)
                    result.retries_performed += sr.retry_count
                    continue

                if self._check_dependencies(step):
                    ready.append(step)
                else:
                    still_pending.append(step)

            if not ready:
                for step in still_pending:
                    sr = StepResult(
                        step_id=step.id,
                        status=StepStatus.BLOCKED,
                        output="Blocked: dependency not met and no more steps can run",
                    )
                    result.step_results.append(sr)
                    self._completed_steps[step.id] = sr
                    self._persist_step(store, plan_id, sr)
                break

            if len(ready) > 1:
                logger.info(
                    "Parallel wave: executing %d steps concurrently: %s",
                    len(ready),
                    [s.id for s in ready],
                )

            async def _run_one(step: ExecutionStep) -> StepResult:
                if self._kill_switch.state == KillSwitchState.TRIGGERED:
                    return StepResult(
                        step_id=step.id,
                        status=StepStatus.SKIPPED,
                        output="Skipped: kill switch triggered",
                    )
                if step.condition and not self._evaluate_condition(step.condition):
                    sr = StepResult(
                        step_id=step.id,
                        status=StepStatus.SKIPPED,
                        output=f"Skipped: condition not met ({step.condition})",
                    )
                    self._completed_steps[step.id] = sr
                    self._persist_step(store, plan_id, sr)
                    return sr

                task_id = None
                if progress:
                    task_id = progress.add_task(
                        f"[{step.id}] {step.description or step.tool or 'executing'}",
                        total=None,
                    )

                sr = await self._execute_step_with_retry(step, interactive)
                self._completed_steps[step.id] = sr
                self._persist_step(store, plan_id, sr)
                self._record_step_feedback(step, sr)

                self._adapt_plan_on_step_result(step, sr, plan, still_pending)
                await self._replan_from_feedback(step, sr, plan, still_pending)

                if progress and task_id is not None:
                    status_icon = "✅" if sr.status == StepStatus.SUCCESS else "❌"
                    progress.update(
                        task_id,
                        description=f"{status_icon} [{step.id}] {step.description}",
                        completed=True,
                    )
                return sr

            tasks = [self._pool.submit(_run_one, s) for s in ready]
            wave_results = await asyncio.gather(*tasks, return_exceptions=True)

            for i, wr in enumerate(wave_results):
                if isinstance(wr, Exception):
                    sr = StepResult(
                        step_id=ready[i].id,
                        status=StepStatus.FAILED,
                        error=str(wr),
                    )
                    self._completed_steps[ready[i].id] = sr
                    self._persist_step(store, plan_id, sr)
                    result.step_results.append(sr)
                elif isinstance(wr, StepResult):
                    result.step_results.append(wr)
                    result.all_findings.extend(wr.findings)
                    result.retries_performed += wr.retry_count

            pending = still_pending

        try:
            await self._pool.cancel_pending()
        except Exception:
            logger.debug("Error resetting sub-agent pool", exc_info=True)
        return result

    def _persist_step(
        self,
        store: OfflineStore | None,
        plan_id: str | None,
        sr: StepResult,
    ) -> None:
        """Helper to persist a step result to the offline store."""
        if store and plan_id:
            store.upsert_step_execution(
                plan_id=plan_id,
                step_id=sr.step_id,
                status=sr.status.value,
                output=sr.output[:5000],
                error=sr.error[:2000],
                findings=sr.findings,
                duration_ms=sr.duration_ms,
                retry_count=sr.retry_count,
                exit_code=sr.exit_code,
            )

    async def _calculate_backoff_delay(self, attempt: int) -> float:
        """Calculate exponential backoff delay with jitter."""
        return await _calculate_backoff_delay_impl(attempt)

    async def _execute_step_with_retry(
        self, step: ExecutionStep, interactive: bool
    ) -> StepResult:
        """Execute a step with automatic retry on transient failures."""
        retry_count = 0
        last_error = None

        no_retry_types = {StepType.REPORT, StepType.ANALYSIS}
        if step.step_type in no_retry_types:
            return await self._execute_step(step, interactive)

        while retry_count <= _MAX_RETRIES:
            try:
                result = await self._execute_step(step, interactive)

                if result.status == StepStatus.SUCCESS:
                    result.retry_count = retry_count
                    return result

                if result.status == StepStatus.FAILED and retry_count < _MAX_RETRIES:
                    last_error = result.error
                    if _is_transient_error_impl(result.error):
                        retry_count += 1
                        delay = await self._calculate_backoff_delay(retry_count - 1)
                        logger.warning(
                            "Step %s failed (retryable): %s. Retrying in %.1fs (attempt %d/%d)",
                            step.id,
                            result.error,
                            delay,
                            retry_count,
                            _MAX_RETRIES,
                        )
                        await asyncio.sleep(delay)
                        continue

                result.retry_count = retry_count
                return result

            except Exception as e:
                last_error = str(e)
                if retry_count < _MAX_RETRIES and _is_transient_error_impl(last_error):
                    retry_count += 1
                    delay = await self._calculate_backoff_delay(retry_count - 1)
                    logger.warning(
                        "Step %s raised exception (retryable): %s. Retrying in %.1fs (attempt %d/%d)",
                        step.id,
                        str(e),
                        delay,
                        retry_count,
                        _MAX_RETRIES,
                    )
                    await asyncio.sleep(delay)
                    continue
                else:
                    return StepResult(
                        step_id=step.id,
                        status=StepStatus.FAILED,
                        error=last_error or str(e),
                        retry_count=retry_count,
                    )

        return StepResult(
            step_id=step.id,
            status=StepStatus.FAILED,
            error=f"Max retries ({_MAX_RETRIES}) exceeded: {last_error}",
            retry_count=retry_count,
        )

    def _is_transient_error(self, error: str) -> bool:
        """Check if error is transient and retryable."""
        return _is_transient_error_impl(error)

    async def _execute_step(self, step: ExecutionStep, interactive: bool) -> StepResult:
        """Execute a single step based on its type, routing through engine lifecycle hooks."""
        if step.step_type == StepType.TOOL_RUN:
            return await self._run_tool_step(step, interactive)
        if step.step_type == StepType.SHELL_CMD:
            return await self._run_shell_step(step, interactive)
        return await self._executor.execute_step(step, interactive)

    async def _run_tool_step(
        self, step: ExecutionStep, interactive: bool
    ) -> StepResult:
        """Execute a registered security tool — delegates to ToolExecutor with lifecycle hooks."""
        tool_name = step.tool or ""

        if tool_name != "__all__":
            resolved = self._resolver.resolve(tool_name, step.args)
            if not resolved.is_safe and any(
                "not found on PATH" in w for w in resolved.warnings
            ):
                installed = await self._try_install_tool(tool_name)
                if installed:
                    self._resolver = DynamicResolver(
                        registered_tools={
                            t.binary: t.path
                            for t in self._discovered_tools
                        }
                        | {t.name: t.path for t in self._discovered_tools}
                    )

        original_args_str = " ".join(step.args)
        confirmed_str = await self._apply_permission_gate(
            step, tool_name, original_args_str, interactive
        )
        if confirmed_str == _FORBIDDEN_MARKER:
            return StepResult(
                step_id=step.id,
                status=StepStatus.BLOCKED,
                error="Forbidden by permission gate",
            )
        if confirmed_str != original_args_str:
            step = ExecutionStep(
                id=step.id,
                step_type=step.step_type,
                tool=step.tool,
                command=step.command,
                args=confirmed_str.split(),
                target=step.target,
                depends_on=step.depends_on,
                condition=step.condition,
                timeout=step.timeout,
                description=step.description,
                metadata=step.metadata,
            )

        sr = await self._executor.execute_step(step, interactive)

        if sr.findings:
            for f in sr.findings:
                notification_center.finding(
                    tool=tool_name,
                    severity=f.get("severity", "info"),
                    description=f.get("description", str(f)),
                    target=step.target or "",
                )
            for f in sr.findings:
                self._graph.ingest_finding(f, tool=tool_name)

        if self._learning_memory is not None and confirmed_str != original_args_str:
            try:
                self._learning_memory.record_with_correction(
                    tools=[tool_name],
                    original=f"{tool_name} {original_args_str}",
                    corrected=confirmed_str,
                    task=step.description or "",
                    findings_after=len(sr.findings),
                    duration_ms=sr.duration_ms,
                    success=sr.status == StepStatus.SUCCESS,
                    target=step.target or "",
                )
            except Exception as exc:
                logger.debug("Failed to record tool correction: %s", exc)

        if self._session_logger is not None and self._current_log_session_id:
            try:
                self._session_logger.add_command(
                    session_id=self._current_log_session_id,
                    input_text=f"{tool_name} {confirmed_str}",
                    execution_time_ms=sr.duration_ms,
                    output_summary=(
                        f"{len(sr.findings)} findings"
                        if sr.findings
                        else f"exit: {sr.exit_code}"
                    ),
                )
                self._session_logger.track_tool_usage(
                    self._current_log_session_id, tool_name
                )
            except Exception as exc:
                logger.debug("Session log recording failed: %s", exc)

        return sr

    async def _run_shell_step(
        self, step: ExecutionStep, interactive: bool
    ) -> StepResult:
        """Execute a shell command — delegate to ToolExecutor with engine-level extras."""
        command = step.command or ""
        if not command:
            return StepResult(
                step_id=step.id,
                status=StepStatus.SKIPPED,
                output="No command specified",
            )

        parts = command.split()
        base_cmd = parts[0]
        resolved = self._resolver.resolve(base_cmd, parts[1:] + list(step.args))
        if not resolved.is_safe and any(
            "not found on PATH" in w for w in resolved.warnings
        ):
            installed = await self._try_install_tool(base_cmd)
            if installed:
                self._resolver = DynamicResolver(
                    registered_tools={
                        t.binary: t.path for t in self._discovered_tools
                    }
                    | {t.name: t.path for t in self._discovered_tools}
                )

        confirmed_str = await self._apply_permission_gate(
            step, command, command, interactive
        )
        if confirmed_str == _FORBIDDEN_MARKER:
            return StepResult(
                step_id=step.id,
                status=StepStatus.BLOCKED,
                error="Forbidden by permission gate",
            )
        if confirmed_str != command:
            step = ExecutionStep(
                id=step.id,
                step_type=step.step_type,
                tool=step.tool,
                command=confirmed_str,
                args=step.args,
                target=step.target,
                depends_on=step.depends_on,
                condition=step.condition,
                timeout=step.timeout,
                description=step.description,
                metadata=step.metadata,
            )

        sr = await self._executor.execute_step(step, interactive)

        if self._learning_memory is not None and command != confirmed_str:
            try:
                self._learning_memory.record_with_correction(
                    tools=[base_cmd],
                    original=command,
                    corrected=confirmed_str,
                    task=step.description or "",
                    duration_ms=sr.duration_ms,
                    success=sr.status == StepStatus.SUCCESS,
                    target=step.target or "",
                )
            except Exception as exc:
                logger.debug("Failed to record shell correction: %s", exc)

        if self._session_logger is not None and self._current_log_session_id:
            try:
                self._session_logger.add_command(
                    session_id=self._current_log_session_id,
                    input_text=confirmed_str,
                    execution_time_ms=sr.duration_ms,
                    output_summary=f"exit: {sr.exit_code}",
                )
                self._session_logger.track_tool_usage(
                    self._current_log_session_id, base_cmd
                )
            except Exception as exc:
                logger.debug("Session log recording failed: %s", exc)

        return sr


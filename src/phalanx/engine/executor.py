    def _check_dependencies(self, step: ExecutionStep) -> bool:
        """Check if all dependencies have completed successfully."""
        for dep_id in step.depends_on:
            dep_result = self._completed_steps.get(dep_id)
            if dep_result is None:
                return False
            if dep_result.status not in (StepStatus.SUCCESS, StepStatus.SKIPPED):
                return False
        return True

    def _evaluate_condition(self, condition: str) -> bool:
        """Pillar 3: Step Condition Evaluator.
        Evaluates a step condition dynamically based on completed steps
        and the live KnowledgeGraph state. Supports negations 'not (...)',
        step status checkers, findings checkers, and graph target states.
        """
        condition = condition.strip()

        if condition.startswith("not ") or condition.startswith("not("):
            if condition.startswith("not "):
                sub = condition[4:].strip()
            else:
                sub = condition[3:].strip()
            if sub.startswith("(") and sub.endswith(")"):
                sub = sub[1:-1].strip()
            return not self._evaluate_condition(sub)

        if condition.endswith(".success"):
            step_id = condition[:-8].strip()
            res = self._completed_steps.get(step_id)
            return res is not None and res.status == StepStatus.SUCCESS

        if condition.endswith(".failed"):
            step_id = condition[:-8].strip()
            res = self._completed_steps.get(step_id)
            return res is not None and res.status == StepStatus.FAILED

        if "findings.count > 0" in condition:
            total_findings = sum(
                len(r.findings) for r in self._completed_steps.values()
            )
            return total_findings > 0

        port_match = re.match(r"^port_(\d+)_open$", condition)
        if port_match:
            port_num = int(port_match.group(1))
            from ..knowledge_graph import NodeType

            ports = self._graph.find_nodes(NodeType.PORT)
            for p in ports:
                if p.properties.get("port") == port_num:
                    return True
            return False

        service_match = re.match(r"^service_([a-zA-Z0-9_\-]+)_running$", condition)
        if service_match:
            svc_name = service_match.group(1).lower()
            from ..knowledge_graph import NodeType

            services = self._graph.find_nodes(NodeType.SERVICE)
            for s in services:
                if s.label.lower() == svc_name:
                    return True
            return False

        if condition == "vulnerability_found":
            from ..knowledge_graph import NodeType

            vulns = self._graph.find_nodes(NodeType.VULNERABILITY)
            return len(vulns) > 0

        return True

    def _record_step_feedback(self, step: ExecutionStep, sr: StepResult) -> None:
        """Feed step outcomes into XI context/predictor and skill profiler."""
        tool_name = step.tool or (
            step.command.split()[0] if step.command else "analysis"
        )
        target = step.target or ""
        self._xi_tracker.record_execution(
            tool=tool_name,
            target=target,
            duration_ms=sr.duration_ms,
            success=sr.status == StepStatus.SUCCESS,
            findings_count=len(sr.findings),
        )
        if step.command:
            self._xi_predictor.learn(step.command)
        elif step.tool:
            cmd = " ".join([step.tool, *step.args, step.target or ""]).strip()
            self._xi_predictor.learn(cmd)
        else:
            cmd = ""

        self._get_skill_profiler().record_command(
            command=cmd or "",
            tool=tool_name,
            success=sr.status == StepStatus.SUCCESS,
        )

    async def _replan_from_feedback(
        self,
        step: ExecutionStep,
        sr: StepResult,
        plan: ExecutionPlan,
        still_pending: list[ExecutionStep],
    ) -> None:
        """Trigger planner re-evaluation after failures or zero-finding outcomes."""
        if self._replan_attempts >= self._max_replans:
            return
        if not (
            sr.status == StepStatus.FAILED
            or (sr.status == StepStatus.SUCCESS and len(sr.findings) == 0)
        ):
            return

        trigger = "step_failed" if sr.status == StepStatus.FAILED else "zero_findings"
        replan_ctx = self._build_context()
        replan_ctx.update(
            {
                "trigger_step": step.to_dict(),
                "trigger_result": {
                    "status": sr.status.value,
                    "error": sr.error[:500],
                    "output": sr.output[:1000],
                    "findings_count": len(sr.findings),
                },
                "completed_steps": [
                    s.to_dict() for s in plan.steps if s.id in self._completed_steps
                ],
                "pending_steps": [s.to_dict() for s in still_pending],
            }
        )
        adaptive = await self._planner.replan(
            instruction=plan.raw_instruction or "",
            context=replan_ctx,
            trigger_step=step,
            trigger_reason=trigger,
        )
        if not adaptive or not adaptive.steps:
            return

        existing_ids = {s.id for s in plan.steps} | {s.id for s in still_pending}
        injected: list[ExecutionStep] = []
        for i, candidate in enumerate(adaptive.steps, 1):
            base_id = candidate.id or f"{step.id}_replan_{i}"
            new_id = base_id
            while new_id in existing_ids:
                new_id = f"{base_id}_{i}"
            candidate.id = new_id
            if step.id not in candidate.depends_on:
                candidate.depends_on.append(step.id)
            existing_ids.add(new_id)
            injected.append(candidate)

        if injected:
            still_pending[:0] = injected
            plan.steps.extend(injected)
            self._replan_attempts += 1
            logger.info(
                "Adaptive re-plan injected %d step(s) after %s (%s)",
                len(injected),
                step.id,
                trigger,
            )

    def _adapt_plan_on_step_result(
        self,
        step: ExecutionStep,
        sr: StepResult,
        plan: ExecutionPlan,
        still_pending: list[ExecutionStep],
    ) -> None:
        """Pillar 3: Dynamic Plan Mutator.
        Analyzes the result of a completed step and mutates remaining/pending steps
        to inject retries, fallbacks, or privilege checks at runtime.
        """
        if step.tool == "nmap" and (
            "Host seems down" in (sr.error or "")
            or "Host seems down" in (sr.output or "")
            or "try -Pn" in (sr.error or "")
            or "try -Pn" in (sr.output or "")
        ):
            new_args = list(step.args)
            if "-Pn" not in new_args:
                new_args.append("-Pn")
            retry_step = ExecutionStep(
                id=f"{step.id}_retry_pn",
                step_type=StepType.TOOL_RUN,
                tool="nmap",
                args=new_args,
                target=step.target,
                description=f"Retry nmap with -Pn (ping bypass) for {step.target}",
                timeout=step.timeout,
            )
            for s in still_pending:
                if step.id in s.depends_on:
                    s.depends_on.remove(step.id)
                    s.depends_on.append(retry_step.id)
            still_pending.insert(0, retry_step)
            logger.info(
                "Dynamic Mutation: Injected nmap -Pn retry step %s", retry_step.id
            )

        elif step.tool == "nikto" and sr.status == StepStatus.FAILED:
            fallback_step = ExecutionStep(
                id=f"{step.id}_fallback_nuclei",
                step_type=StepType.TOOL_RUN,
                tool="nuclei",
                target=step.target,
                description=f"Fallback: nuclei vuln scan against {step.target} after nikto failure",
            )
            for s in still_pending:
                if step.id in s.depends_on:
                    s.depends_on.remove(step.id)
                    s.depends_on.append(fallback_step.id)
            still_pending.insert(0, fallback_step)
            logger.info(
                "Dynamic Mutation: Swapped/added nuclei fallback %s", fallback_step.id
            )

        elif (
            step.tool == "gobuster"
            and sr.status == StepStatus.SUCCESS
            and len(sr.findings) == 0
        ):
            fallback_step = ExecutionStep(
                id=f"{step.id}_fallback_nikto",
                step_type=StepType.TOOL_RUN,
                tool="nikto",
                target=step.target,
                description=f"Zero-findings fallback: nikto scan against {step.target}",
            )
            for s in still_pending:
                if step.id in s.depends_on:
                    s.depends_on.remove(step.id)
                    s.depends_on.append(fallback_step.id)
            still_pending.insert(0, fallback_step)
            logger.info(
                "Dynamic Mutation: Injected nikto fallback %s due to zero gobuster findings",
                fallback_step.id,
            )

        elif step.step_type == StepType.SHELL_CMD:
            is_permission_denied = False
            err_msg = ((sr.error or "") + (sr.output or "")).lower()
            for indicator in (
                "permission denied",
                "access denied",
                "not allowed",
                "requires elevation",
            ):
                if indicator in err_msg:
                    is_permission_denied = True
                    break

            if is_permission_denied:
                import sys

                priv_cmd = "whoami /priv" if sys.platform == "win32" else "sudo -l"
                priv_step = ExecutionStep(
                    id=f"{step.id}_priv_check",
                    step_type=StepType.SHELL_CMD,
                    command=priv_cmd,
                    description=f"Privilege check due to access denial on step {step.id}",
                )
                still_pending.insert(0, priv_step)
                logger.info(
                    "Dynamic Mutation: Injected privilege check %s after permission error",
                    priv_step.id,
                )

